        <p>

            Dehradun is the capital and the most populous city of the Indian state of Uttarakhand. It is the administrative headquarters of the eponymous district and is governed by the Dehradun Municipal Corporation, with the Uttarakhand Legislative Assembly holding its winter sessions in the city as its winter capital.[13] Part of the Garhwal region, and housing the headquarters of its Divisional Commissioner. Dehradun is one of the "Counter Magnets" of the National Capital Region (NCR) being developed as an alternative center of growth to help ease the migration and population explosion in the Delhi metropolitan area and to establish a smart city in the Himalayas.[14][15] It is the third largest city in the Himalayas after Kathmandu and Srinagar.
            Dehradun is located in the Doon Valley on the foothills of the Himalayas nestled between Song river, a tributary of Ganga on the east and the Asan river, a tributary of Yamuna on the west. The city is noted for its picturesque landscape and slightly milder climate and provides a gateway to the surrounding region.

